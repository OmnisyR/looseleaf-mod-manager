class Translator:
    def __init__(self, language: str) -> None:
        self.language = language if language in LANGUAGES else DEFAULT_LANGUAGE

    def set_language(self, language: str) -> None:
        self.language = language if language in LANGUAGES else DEFAULT_LANGUAGE

    def t(self, key: str, **kwargs: object) -> str:
        value = TRANSLATIONS.get(self.language, {}).get(key)
        if value is None:
            value = TRANSLATIONS[DEFAULT_LANGUAGE].get(key, key)
        return value.format(**kwargs) if kwargs else value


def translate(language: str, key: str, **kwargs: object) -> str:
    return Translator(language).t(key, **kwargs)
